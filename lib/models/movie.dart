class Movie {
  final int id;
  final String title;
  final String genre;
  final String year;
  final double rating;
  final String description;
  final String imageUrl;

  const Movie({
    required this.id,
    required this.title,
    required this.genre,
    required this.year,
    required this.rating,
    required this.description,
    required this.imageUrl,
  });
}
